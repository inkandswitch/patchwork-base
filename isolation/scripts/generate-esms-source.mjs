/**
 * Generates `src/esms-source.generated.ts` — the es-module-shims source inlined
 * as a string export.
 *
 * The isolation iframe has an opaque origin and cannot fetch anything from the
 * host (that is the whole point of the sandbox), so the shim source must be
 * baked into the iframe's srcdoc. Core's bootloader serves this file at
 * `/es-module-shims.js` and the host `fetch`es it; a patchwork-base package has
 * no such host-served path, so we bundle the source at build time instead.
 *
 * We read the SAME variant core serves — `es-module-shims/wasm`
 * (`require.resolve` in core's importmap-plugin.ts → `es-module-shims.wasm.js`)
 * — so the iframe's `shimMode`/`source`-hook behavior is identical.
 *
 * Run as a prebuild step (see package.json). `tsc` then compiles the generated
 * module like any other source — no bundler `?raw` loader needed, which keeps
 * the `.toString()`-delivered iframe functions free of injected helpers.
 */

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { createRequire } from "node:module";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const here = dirname(fileURLToPath(import.meta.url));
const outFile = join(here, "..", "src", "esms-source.generated.ts");

const esmsPath = require.resolve("es-module-shims/wasm");
const source = readFileSync(esmsPath, "utf8");

const banner =
  "// AUTO-GENERATED by scripts/generate-esms-source.mjs — do not edit.\n" +
  "// Source: es-module-shims/wasm (matches the variant core serves at /es-module-shims.js).\n";

// Embed via JSON.stringify so any characters in the shim source are safely escaped.
const contents = `${banner}export const esmsSource: string = ${JSON.stringify(
  source
)};\n`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, contents);

console.log(
  `[generate-esms-source] wrote ${outFile} (${source.length} bytes from ${esmsPath})`
);
